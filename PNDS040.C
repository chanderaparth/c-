 /* For loop pattern - 2
	1
       12
      123
     1234
    12345
*/

#include <stdio.h>
#include <conio.h>

int main() {
  int i, j, k, no;

  clrscr();

  printf("Enter end no: ");
  scanf("%d", &no);

  for(i=1; i<=no; i++) {
    for(k=1; k<=(no-i); k++) {
      printf(" ");
    }
    for(j=1; j<=i; j++) {
      printf("%d",j);
    }
    printf("\n");
  }

  getch();
  return 0;
}