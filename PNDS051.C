/* Single dimensional arrays with direct value defining */

#include <stdio.h>
#include <conio.h>

int main() {
  int a, no[] = {1,2,3,4,5};
  char ch[] = {'P','N','D','S','\0'};

  clrscr();
  for(a=0; a<5; ++a)  {
    printf("no[%d] = %d\t",a,no[a]);
    printf("ch[%d] = %c\n",a,ch[a]);
  }

  getch();
  return 0;
}