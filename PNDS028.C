/* Program to display number in words with If, Else If and Else statement */

#include <stdio.h>
#include <conio.h>

int main() {
  int number;
  clrscr();

  printf("Please enter the number 1 to 5 to display number in words.\n");
  scanf("%d", &number);

  if(number == 1)
      printf("One");
  else if(number == 2)
      printf("Two");
  else if(number == 3)
      printf("Three");
  else if(number == 4)
      printf("Four");
  else if(number == 5)
      printf("Five");
  else
    printf("Please enter number between 1 to 5 only\n");

  getch();
  return 0;
}